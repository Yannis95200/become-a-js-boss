
let a = 5;
let b = 10;


let resultat;
let op = "*";


if (op === "+") {
    resultat = a + b
    console.log(resultat)
}else if (op === "-"){
    resultat = a - b
    console.log(resultat)
}else if (op === "*") {
    resultat = a * b
    console.log(resultat)
}else if (op === "/") {
    resultat = a / b
    console.log(resultat)
}




